package com.ice.po;

public class Comment {

	private int u_id;
	private int m_id;
	private String c_date;
	private String c_content;
	private String u_name;
	private String u_images;
	
	public int getU_id() {
		return u_id;
	}
	public void setU_id(int uId) {
		u_id = uId;
	}
	public int getM_id() {
		return m_id;
	}
	public void setM_id(int mId) {
		m_id = mId;
	}
	public String getC_date() {
		return c_date;
	}
	public void setC_date(String cDate) {
		c_date = cDate;
	}
	public String getC_content() {
		return c_content;
	}
	public void setC_content(String cContent) {
		c_content = cContent;
	}
	public String getU_name() {
		return u_name;
	}
	public void setU_name(String uName) {
		u_name = uName;
	}
	public String getU_images() {
		return u_images;
	}
	public void setU_images(String uImages) {
		u_images = uImages;
	}
	
	
}
